package com.ws.altanet.model;



public class Academic_record {
		Long academics_id;
		String course;
		String year_level;
		Long user_id;
		public Long getAcademics_id() {
			return academics_id;
		}
		public void setAcademics_id(Long academics_id) {
			this.academics_id = academics_id;
		}
		public String getCourse() {
			return course;
		}
		public void setCourse(String course) {
			this.course = course;
		}
		public String getYear_level() {
			return year_level;
		}
		public void setYear_level(String year_level) {
			this.year_level = year_level;
		}
		public Long getUser_id() {
			return user_id;
		}
		public void setUser_id(Long user_id) {
			this.user_id = user_id;
		}
		
		
}
